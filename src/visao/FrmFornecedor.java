    private void jComboBoxBairroMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBoxBairroMouseEntered
        try {
        conn.conexao();
        conn.executaSQL("select * from bairro inner join Cidade on bairro.id_cidade=cidade.id_cidade inner join estados on cidade.id_estado=estados.id_estado where nome_bairro = '"+jComboBoxBairro.getSelectedItem()+"' ");
        conn.rs.first();
        jLabelCidade.setText(conn.rs.getString("nome_cidades"));
        jLabelEstado.setText(conn.rs.getString("sigla_estado"));
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane,"erro selecionar bairro!\nERRO:"+ex);
        }
    }//GEN-LAST:event_jComboBoxBairroMouseEntered

